namespace Modern.Forms;

/// <summary>
/// Specifies a built-in system theme.
/// </summary>
public enum BuiltInTheme
{
    /// <summary>
    /// Uses the built-in theme that matches the OS.
    /// </summary>
    Default,

    /// <summary>
    /// Uses the "Light" built-in theme.
    /// </summary>
    Light,

    /// <summary>
    /// Uses the "Dark" built-in theme.
    /// </summary>
    Dark,
    /// <summary>
    /// Modern clean light theme, inspired by current SaaS / Tailwind style UI.
    /// </summary>
    Modern,

    /// <summary>
    /// Windows Metro style theme, flat, clean, blue-accented.
    /// </summary>
    Metro
}

